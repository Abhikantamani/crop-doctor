def get_weather_risk(location):
    try:
        url = f"https://wttr.in/{location}?format=j1"
        response = requests.get(url, timeout=5)
        data = response.json()
        temp = float(data['current_condition'][0]['temp_C'])
        humidity = float(data['current_condition'][0]['humidity'])
        desc = data['current_condition'][0]['weatherDesc'][0]['value']
        risks = []
        if humidity > 80:
            risks.append("🍄 High humidity — high risk of **fungal diseases** (blast, blight)")
        if humidity > 70 and temp > 25:
            risks.append("🦠 Warm & humid — watch for **bacterial infections**")
        if temp > 35:
            risks.append("☀️ Very hot — risk of **heat stress and wilting**")
        if temp < 15:
            risks.append("❄️ Cool weather — risk of **powdery mildew**")
        if not risks:
            risks.append("✅ Weather conditions look safe for crops today")
        return {"temp": temp, "humidity": humidity, "desc": desc, "risks": risks}
    except:
        return None

