# Database setup
def init_db():
    conn = sqlite3.connect("diagnoses.db")
    cursor = conn.cursor()
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS diagnoses (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            crop_name TEXT,
            disease TEXT,
            severity TEXT,
            yield_loss TEXT,
            treatment TEXT,
            medicine TEXT,
            language TEXT,
            date TEXT
        )
    """)
    conn.commit()
    conn.close()

def save_diagnosis(crop_name, disease, severity, yield_loss, treatment, medicine, language):
    conn = sqlite3.connect("diagnoses.db")
    cursor = conn.cursor()
    cursor.execute("""
        INSERT INTO diagnoses (crop_name, disease, severity, yield_loss, treatment, medicine, language, date)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?)
    """, (crop_name, disease, severity, yield_loss, treatment, medicine, language, datetime.now().strftime("%Y-%m-%d %H:%M")))
    conn.commit()
    conn.close()

def get_all_diagnoses():
    conn = sqlite3.connect("diagnoses.db")
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM diagnoses ORDER BY date DESC")
    rows = cursor.fetchall()
    conn.close()
    return rows

def encode_image(image_file):
    img = Image.open(image_file)
    if img.mode != "RGB":
        img = img.convert("RGB")
    buffer = io.BytesIO()
    img.save(buffer, format="JPEG")
    return base64.standard_b64encode(buffer.getvalue()).decode("utf-8")

